package fundamentos;

public class AreaCircunferencia {
    public static void main(String[] args) {
        double raio = 3.4; // Cria variável
        final double PI = 3.14159; // Cria constante

        double area = PI * raio * raio;

        System.out.println("Área = " + area + "m2.");

    }
}
